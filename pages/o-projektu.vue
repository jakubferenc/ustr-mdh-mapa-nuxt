          p Jednotlivé mapy vizualizují dějinná témata, jež byla podstatná pro kulturní politiku v Československu v období 1948-1989. Kromě jednotlivých muzeí, památníků a artefaktů mapy zachycují i dynamiku našeho vzpomínání. Objevujte s námi místa paměti, na která postupně zapomínáme.
          p Výzkum, kterým je podložena prezentovaná sada interaktivních map, je výsledkem práce s fondy <a href="https://www.mdh21.cz/stranka/o-sbirce-mdh/">Sbírky Muzea dělnického hnutí</a> spravované Národním muzeem. Tato pramenná kolekce vznikla v roce 1990 sloučením sbírek Muzea Klementa Gottwalda, Muzea V. I. Lenina a Muzea Julia Fučíka. Až do roku 2014, kdy jí převzalo Národní muzeum, ji spravovala organizace Muzeum dělnického hnutí o.p.s. Jedná se o rozsáhlý soubor artefaktů, archiválií a knih odrážející původní cíle politicko-propagandistických institucí, jež je shromažďovaly od začátku padesátých let do konce let osmdesátých. Dělo se tak s cílem vytvářet a prezentovat heroický obraz dělnického hnutí, zvláště KSČ a jejích čelních představitelů. Při vytváření interaktivních map se jejich autoři snažili o revizi uvedeným směrem zaměřené politiky paměti vázané na klíčové události a osobnosti stranického výkladu dějin, pochopení jeho mechanismů, inventuru podobně zaměřených a dávno zaniklých československých muzejních institucí a zároveň o nasvícení této problematiky optikou nástrojů kulturních dějin či paměťových studií. Vznikla tak svébytná interpretace těchto pramenů.
